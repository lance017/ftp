package com.lance.ftp.Common;

public enum  ResponseCode {

    SUCCESS(0,"success"),
    ERROR(1,"error");

    private final int code;
    private final String msg;

    ResponseCode(int code, String msg) {
        this.code = code;
        this.msg = msg;
    }

    public int getCode() {
        return code;
    }

    public String getMsg() {
        return msg;
    }
}
